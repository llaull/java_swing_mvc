package model;

import java.io.Serializable;

/**
 *
 * @author Arkesys
 */
public class GameCharcaterModel implements Serializable{
    
    private String type;
    private int power;

    GameCharcaterModel(int i, String plup) {
        this.power = i;
        this.type = plup;
    }

    /**
     * @return the type
     */
    public String getType() {
        return this.type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the power
     */
    public int getPower() {
        return power;
    }

    /**
     * @param power the power to set
     */
    public void setPower(int power) {
        this.power = power;
    }
    
}
